    class TypingReceiver implements Runnable {
       @Override
       public void run() {
           try {
               MulticastSocket typingSocket = new MulticastSocket(6000);
               InetAddress group = InetAddress.getByName("230.0.0.3");
               typingSocket.joinGroup(group);

               byte[] buf = new byte[256];

               while (true) {
                   DatagramPacket packet = new DatagramPacket(buf, buf.length);
                   typingSocket.receive(packet);

                   String msg = new String(packet.getData(), 0, packet.getLength());

                   if (msg.startsWith("TYPING:")) {
                       String user = msg.substring(7);
                       if (!user.equals(MulticastClient.name)) {
                           typingLabel.setText(user + " is typing...");
                       }
                   }
                   else if (msg.startsWith("STOP_TYPING:")) {
                       typingLabel.setText("");
                   }
               }
           } catch (Exception e) {
               e.printStackTrace();
           }
       }
   }
